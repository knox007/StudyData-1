#include <stdio.h>

int main(void)
{
	//*	1.	정수의 출력과 서식문자.
	printf("Hello World!!\n");	//	문자열 출력.								

	printf("%d\n", 3);			//	정수 출력.
								//	%d : 정수형 10진수 서식문자.

	printf("%d %d\n", 52, 153);	//	출력의 대상이 순서대로 서식문자에 적용된다.

	//*/



	/*	2.	다양한 출력 형태의 조합
	printf("My age : %d \n", 20);

	printf("%d is my Lucky Number \n", 7);

	printf("Good \nJob \nGame Academy\n");
	//*/

	return 0;
}



/*
///// printf의 기본적인 이해

	1.	정수의 출력과 서식문자.
		-	문자열 외에도 다양한 출력이 가능.
		-	서식문자를 사용하여 정수, 실수등을 출력할 수 있다.
			서식문자 : 출력의 형태 지정.



*/